/* ================================ [ FUNCTIONS ] ============================================== */
uint8_t inb(uint32_t p) {
  return readb(p);
}
uint16_t inw(uint32_t p) {
  return readw(p);
}
uint32_t inl(uint32_t p) {
  return readl(p);
}

void outb(uint32_t p, uint8_t v) {
  writeb(p, v);
}

void outw(uint32_t p, uint16_t v) {
  writew(p, v);
}

void outl(uint32_t p, uint32_t v) {
  writel(p, v);
}